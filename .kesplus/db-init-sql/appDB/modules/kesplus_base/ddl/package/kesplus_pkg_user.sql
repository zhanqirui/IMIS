     * @param id 用户id
     * @param deptId 部门id
     * @Return boolean
     */
	FUNCTION set_default_dept(id varchar, deptId varchar)RETURN boolean;
    /**
     * @Description 设置登录默认组织
     * @param id 用户id
     * @param deptId 部门id
     * @Return boolean
     */
	FUNCTION set_login_dept(id varchar, deptId varchar)RETURN boolean;
    /**
     * @Description 用户分页查询-根据组织id
     * @param page 当前页
     * @param size 每页个数
     * @param blurry 查询内容
     * @param enabled 启用状态
     * @param orderColumns 排序数据
     * @param deptId 部门id
     * @Return 用户信息列表
     */
